package day05_ifStatement;

public class c7_multiIfStatement {

    public static void main(String[] args) {
  ///check 2 nums
//first check if they re equal
//second check is of them is bigger


int x=25;
int y=5;

if (x==y) {// java will first check this block
    System.out.println();
}else if(x>y){// it will check this as next
    System.out.println(x+"is bigger than"+y);
    }else{// if 2 before is not true, the last one will print
    System.out.println(x+"is smaller than"+y);

    }

}











}
